package com.common.bean;

public class Son {
	private int id;
	private String name;
	private Woman wife;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Woman getWife() {
		return wife;
	}
	public void setWife(Woman wife) {
		this.wife = wife;
	}
	
}
